const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}
//If the "keyword" is removed then it will execute all the code after that value if match except the default case, thus default case will not be executed if break keyword is removed.